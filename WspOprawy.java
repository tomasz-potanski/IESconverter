public class WspOprawy {
    double pozycjaNaDlugosci;
    double pozycjaNaSzerokosci;
    double wysokosc;


    public WspOprawy(
            double pozycjaNaDlugosci,
            double pozycjaNaSzerokosci,
            double wysokosc) {
        this.pozycjaNaDlugosci = pozycjaNaDlugosci;
        this.pozycjaNaSzerokosci = pozycjaNaSzerokosci;
        this.wysokosc = wysokosc;
    }
}
